create table xxpc_users (
    pc_user_id             number generated by default on null as identity,
    employee_id            number not null,
    employee_number        varchar2(30),
    employee_name          varchar2(240) not null,
    user_name              varchar2(100) not null,
    responsibility_id      number,
    operating_unit_id      number,
    currency_code          varchar2(15) default 'AED' not null,
    cash_limit_amount      number(15,2) not null,
    warning_limit_amount   number(15,2),
    active_flag            varchar2(1) default 'Y' not null,
    start_date             date default trunc(sysdate) not null,
    end_date               date,
    created_by             number,
    creation_date          date default sysdate not null,
    last_updated_by        number,
    last_update_date       date default sysdate not null,
    constraint xxpc_users_pk primary key (pc_user_id),
    constraint xxpc_users_u1 unique (user_name),
    constraint xxpc_users_active_ck check (active_flag in ('Y', 'N')),
    constraint xxpc_users_limit_ck check (cash_limit_amount >= 0)
);
