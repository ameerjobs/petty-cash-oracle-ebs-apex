create table xxpc_expenses (
    expense_id          number generated by default on null as identity,
    expense_number      varchar2(30) not null,
    request_id          number,
    pc_user_id          number not null,
    category_id         number not null,
    expense_date        date not null,
    merchant_name       varchar2(240),
    description         varchar2(1000),
    amount              number(15,2) not null,
    tax_amount          number(15,2) default 0 not null,
    currency_code       varchar2(15) default 'AED' not null,
    receipt_reference   varchar2(240),
    status_code         varchar2(30) default 'DRAFT' not null,
    gl_date             date,
    ap_invoice_id       number,
    created_by          number,
    creation_date       date default sysdate not null,
    last_updated_by     number,
    last_update_date    date default sysdate not null,
    constraint xxpc_expenses_pk primary key (expense_id),
    constraint xxpc_expenses_u1 unique (expense_number),
    constraint xxpc_expenses_request_fk foreign key (request_id) references xxpc_requests (request_id),
    constraint xxpc_expenses_user_fk foreign key (pc_user_id) references xxpc_users (pc_user_id),
    constraint xxpc_expenses_category_fk foreign key (category_id) references xxpc_categories (category_id),
    constraint xxpc_expenses_amount_ck check (amount > 0),
    constraint xxpc_expenses_tax_ck check (tax_amount >= 0),
    constraint xxpc_expenses_status_ck check (
        status_code in ('DRAFT', 'SUBMITTED', 'APPROVED', 'REJECTED', 'ACCOUNTED', 'CANCELLED')
    )
);
